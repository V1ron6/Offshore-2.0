/**
 * Order Model
 * Defines the schema for e-commerce orders
 */

const { v4: uuidv4 } = require('uuid');

const orderSchema = {
	id: {
		type: 'auto-increment',
		required: true,
		unique: true
	},
	orderId: {
		type: String,
		required: true,
		unique: true
	},
	userId: {
		type: String,
		required: true
	},
	customerName: {
		type: String,
		required: true
	},
	email: {
		type: String,
		required: true
	},
	items: {
		type: Array,
		required: true
	},
	totalAmount: {
		type: Number,
		required: true,
		min: 0.01
	},
	status: {
		type: String,
		enum: ['pending', 'processing', 'shipped', 'delivered', 'cancelled'],
		default: 'pending'
	},
	shippingAddress: {
		type: Object,
		required: true
	},
	paymentMethod: {
		type: String,
		required: true
	},
	date: {
		type: String,
		required: true
	},
	estimatedDelivery: {
		type: String,
		required: false
	},
	trackingNumber: {
		type: String,
		required: false
	}
};

