/**
 * Product Model
 * Defines the schema for e-commerce products
 */

const productSchema = {
	id: {
		type: 'auto-increment',
		required: true,
		unique: true
	},
	name: {
		type: String,
		required: true,
		minlength: 3,
		maxlength: 100
	},
	description: {
		type: String,
		required: false,
		maxlength: 500
	},
	price: {
		type: Number,
		required: true,
		min: 0.01
	},
	originalPrice: {
		type: Number,
		required: false,
		min: 0.01
	},
	category: {
		type: String,
		enum: ['electronics', 'devices', 'foodstuffs', 'accessories', 'home', 'personal-care', 'beauty', 'books', 'clothing'],
		required: true
	},
	subcategory: {
		type: String,
		required: false
	},
	stock: {
		type: Number,
		required: true,
		default: 0,
		min: 0
	},
	image: {
		type: String,
		required: true,
		default: 'üì¶'
	},
	rating: {
		type: Number,
		required: false,
		min: 0,
		max: 5,
		default: 0
	},
	reviews: {
		type: Number,
		required: false,
		default: 0,
		min: 0
	},
	sku: {
		type: String,
		required: true,
		unique: true
	},
	inStock: {
		type: Boolean,
		default: function() {
			return this.stock > 0;
		}
	},
	featured: {
		type: Boolean,
		default: false
	},
	createdAt: {
		type: Date,
		default: Date.now
	},
	updatedAt: {
		type: Date,
		default: Date.now
	}
};

